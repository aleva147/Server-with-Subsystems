public class OcenaPK {
    private int idKor;
    private int idVid;

    public int getIdKor() {
        return idKor;
    }

    public void setIdKor(int idKor) {
        this.idKor = idKor;
    }

    public int getIdVid() {
        return idVid;
    }

    public void setIdVid(int idVid) {
        this.idVid = idVid;
    }

    @Override
    public String toString() {
        return idKor + "\t" + idVid;
    }
}
