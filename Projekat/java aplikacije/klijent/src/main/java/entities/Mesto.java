package entities;

/**
 *
 * @author PC
 */
public class Mesto {
    private Integer id;
    private String naziv;

    
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }
    
    @Override
    public String toString() {
        return this.id + "\t" + this.naziv;
    }
}
